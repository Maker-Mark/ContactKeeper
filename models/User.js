const mongoose = require("mongoose");

//Make a schema for users
const UserSchema = mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

//Export the mondel
module.exports = mongoose.model("user", UserSchema);
